"""BYO direct-to-B2 upload: the presign endpoint (allowlist, size cap, degrade) and the register
endpoint (key-shape fail-closed, missing object, size cap, decode guard, the shared ingest core).
Network-free: in-memory resolver/log/storage at the same seams the webhook tests use; the presign
happy path runs the REAL boto3 signer (a purely local computation, no network)."""

