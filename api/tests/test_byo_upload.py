async def test_register_happy_path_makes_the_asset_recoverable(
    storage: InMemoryStorage,
) -> None:
    png = _png(21)
    storage.put(_VALID_KEY, png)
    async with _client() as c:
        r = await c.post("/demo/byo/register", json={"objectKey": _VALID_KEY})
        assert r.status_code == 200, r.text
        body = r.json()
        manifest_id = body["manifestId"]
        assert manifest_id.startswith("urn:c2pa:b2-")
        assert body["objectKey"] == _VALID_KEY
        assert body["bucket"] == "rooted-dev"
        assert body["backend"] == "in-memory"  # evidence-based; "backblaze-b2" only on real B2
        assert body["sizeBytes"] == len(png)
        assert body["alreadyRegistered"] is False
        assert body["recoverable"] is True

        # The loop closes: the judge's own asset now recovers by content like every other asset.
        rec = await c.post("/matches/byContent", files={"file": ("mine.png", png, "image/png")})
        assert rec.status_code == 200
        assert rec.json()["matches"][0]["manifestId"] == manifest_id

        # And it is a real transparency-log leaf with a real inclusion proof.
        proof = await c.get(f"/transparency/proof/{manifest_id}")
        assert proof.status_code == 200
        assert proof.json()["serverVerified"] is True


async def test_register_is_idempotent_for_the_same_bytes(storage: InMemoryStorage) -> None:
    png = _png(22)
    storage.put(_VALID_KEY, png)
    async with _client() as c:
        first = await c.post("/demo/byo/register", json={"objectKey": _VALID_KEY})
        second = await c.post("/demo/byo/register", json={"objectKey": _VALID_KEY})
    assert first.json()["alreadyRegistered"] is False
    assert second.json()["alreadyRegistered"] is True
    assert second.json()["manifestId"] == first.json()["manifestId"]


async def test_register_rerun_answers_without_download_or_decode(
    storage: InMemoryStorage, monkeypatch: pytest.MonkeyPatch
) -> None:
    # The idempotency short-circuit: once a key is registered, re-registering it must not fetch or
    # decode the object again (a caller looping register on one valid key gains no amplification).
    png = _png(23)
    storage.put(_VALID_KEY, png)
    async with _client() as c:
        first = await c.post("/demo/byo/register", json={"objectKey": _VALID_KEY})
        assert first.status_code == 200

        downloads = 0

        def counting_get(key: str) -> bytes:
            nonlocal downloads
            downloads += 1
            return png

        monkeypatch.setattr(storage, "get", counting_get)
        second = await c.post("/demo/byo/register", json={"objectKey": _VALID_KEY})
    assert second.status_code == 200
    assert second.json()["alreadyRegistered"] is True
    assert second.json()["manifestId"] == first.json()["manifestId"]
    assert second.json()["sizeBytes"] == len(png)
    assert downloads == 0  # answered from the bounded key cache after only a size check


async def test_register_cache_falls_through_when_stored_size_changes(
    storage: InMemoryStorage,
) -> None:
    # A key re-PUT with different bytes inside the presign window must not get a stale answer:
    # the size mismatch falls through to the full download-and-ingest path.
    first_png = _png(24)
    storage.put(_VALID_KEY, first_png)
    async with _client() as c:
        first = await c.post("/demo/byo/register", json={"objectKey": _VALID_KEY})
        assert first.status_code == 200
        # A different image DIMENSION guarantees a different byte size (two random 64x64 PNGs can
        # coincidentally compress to identical lengths).
        rng = np.random.default_rng(25)
        arr = (rng.random((80, 80, 3)) * 255).astype("uint8")
        buf = io.BytesIO()
        Image.fromarray(arr).save(buf, "PNG")
        second_png = buf.getvalue()
        assert len(second_png) != len(first_png)
        storage.put(_VALID_KEY, second_png)
        second = await c.post("/demo/byo/register", json={"objectKey": _VALID_KEY})
    assert second.status_code == 200
    assert second.json()["manifestId"] != first.json()["manifestId"]
    assert second.json()["alreadyRegistered"] is False
