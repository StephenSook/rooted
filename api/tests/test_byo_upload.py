async def test_upload_url_happy_path_presigns_a_constrained_put(presign_env: None) -> None:
    # The real boto3 SigV4 presign runs (local computation, no network). The URL must target the
    # dev bucket, carry a server-generated byo/ key, expire in 600s, and SIGN the content type so
    # the browser PUT cannot swap it for something outside the allowlist.
    async with _client() as c:
        r = await c.post(
            "/demo/byo/upload-url", json={"contentType": "image/png", "sizeBytes": 1000}
        )
    assert r.status_code == 200, r.text
    body = r.json()
    key = body["objectKey"]
    assert key.startswith("byo/") and key.endswith(".png")
    assert len(key) == len("byo/") + 32 + len(".png")  # uuid4 hex, never a client-supplied name
    assert body["bucket"] == "rooted-dev"
    assert body["expiresInSeconds"] == 600
    assert body["maxBytes"] == sbr._MAX_UPLOAD_BYTES
    parsed = urlparse(body["uploadUrl"])
    assert parsed.scheme == "https"
    assert "backblazeb2.com" in parsed.netloc
    assert parsed.path.endswith(key)
    qs = parse_qs(parsed.query)
    assert qs["X-Amz-Expires"] == ["600"]
    assert "content-type" in qs["X-Amz-SignedHeaders"][0]
    # The presigned URL never leaks the application key (only the standard credential scope).
    assert "fake-app-key" not in body["uploadUrl"]


