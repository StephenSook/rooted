_VALID_KEY = "byo/" + "ab12" * 8 + ".png"  # 32 hex chars, allowlisted extension


def _png(seed: int) -> bytes:
    rng = np.random.default_rng(seed)
    arr = (rng.random((64, 64, 3)) * 255).astype("uint8")
    buf = io.BytesIO()
    Image.fromarray(arr).save(buf, "PNG")
    return buf.getvalue()


@pytest.fixture
def storage(monkeypatch: pytest.MonkeyPatch) -> Iterator[InMemoryStorage]:
    monkeypatch.setenv("B2_BUCKET_DEV", "rooted-dev")
    sbr.set_resolver(Resolver(InMemoryIndex(), FakeWatermarker()))
    sbr.set_log(TransparencyLog())
    st = InMemoryStorage()
    sbr.set_storage(st)
    byo._REGISTERED_KEYS.clear()
    yield st
    byo._REGISTERED_KEYS.clear()
    sbr.set_resolver(None)
    sbr.set_log(None)
    sbr.set_storage(None)


@pytest.fixture
def presign_env(monkeypatch: pytest.MonkeyPatch) -> None:
    monkeypatch.setenv("B2_KEY_ID", "0000key")
    monkeypatch.setenv("B2_APP_KEY", "fake-app-key")
    monkeypatch.setenv("B2_BUCKET_DEV", "rooted-dev")
    monkeypatch.setenv("B2_REGION", "us-west-004")
    monkeypatch.setenv("B2_ENDPOINT", "https://s3.us-west-004.backblazeb2.com")


def _client() -> httpx.AsyncClient:
    return httpx.AsyncClient(transport=ASGITransport(app=app), base_url="http://t")


